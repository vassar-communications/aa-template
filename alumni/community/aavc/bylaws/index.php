<p><em>Adopted June 1980 — Amended June 1992; June 2000; June 2005; June 2008; June 2013; June 2016; June 2018; June 2020.</em></p>

<h3>ARTICLE I. NAME</h3>

<p>The name of this Association shall be the Alumnae/i Association of Vassar College (hereinafter, the “Association”).</p>

<h3>ARTICLE II. PURPOSE</h3>

<p>Founded in 1871, the Association shall lead Vassar alumnae/i to advance the interests and mission of the College.</p>

<h3>ARTICLE III. MEMBERSHIP</h3>

<p>Any Vassar College graduate or former student in good standing shall be a member of the Association once that individual’s designated class graduates. The Association shall have an Annual Meeting, currently held during Reunion Weekend each June, or at such other time as established by Article VIII (hereinafter, the “Annual Meeting”).</p>

<h3>ARTICLE IV. BOARD OF DIRECTORS</h3>

<h4>SEC. 1. NUMBER.</h4>

<ol>
    <li>The Board of Directors (collectively, the “Board” and individually, the “Directors”) shall manage the affairs of the Association.</li>
    <li>All Directors must be members of the Association (<em>see</em>&nbsp;Article III).</li>
    <li>The number of Directors constituting the Board shall number not less than twenty (20) and not more than thirty (30).</li>
    <li>As used in these Bylaws, the terms “entire Board,” “Board” or “Board of Directors” means the total number of Directors entitled to vote.</li>
    <li>All Directors serve four year terms, commencing on July 1 of the year that they are elected and ending on June 30 four years thereafter, unless resignation or removal occurs earlier (<em>see</em>&nbsp;Article IV, Sec. 6).</li>
    <li>The Nominating &amp; Governance Committee shall consult with the Officers (<em>see</em>&nbsp;Article V) and eligible Directors, and assign each non-Officer Director to one or more of the following roles, pursuant to Article IX, Section 2:
        <ol>
            <li>Chair of a Committee, as specified in Article IX, Sections 2, 3, 4, 5, 6, 7 or 8.</li>
            <li>Member of at least two Committees, as specified in Article IX, Sections 2, 3, 4, 5, 6, 7 or 8.</li>
            <li>Specific roles at the discretion of the Board.</li>
        </ol>
    </li>
    <li>The same Director may not occupy more than two committee chair positions simultaneously.</li>
    <li>The Board shall include at least one member of the Association who graduated from Vassar College within six (6) years of that individual’s election.</li>
    <li>Terms for Board positions are listed in Appendix A. Descriptions for Board positions are listed in Appendix B.</li>
</ol>

<h4>SEC. 2. MEETINGS.</h4>

<ol>
    <li>The Board shall meet three (3) times annually. Additionally, the Board may meet at the call of the President, or at the written request of eight (8) Directors filed with the Associate Vice President for Alumnae/i Engagement and Executive Director of the Association (hereinafter, the “Associate Vice President”) (<em>see</em>&nbsp;Article VI).&nbsp;In exceptional circumstances, as determined by the Executive Committee, any such meeting may be conducted electronically.</li>
    <li>Notice of each meeting shall be given at least ten (10) days in advance. Unless otherwise specified, any and all business may be transacted at any meeting of the Board.</li>
    <li>Any Director may participate in a meeting by means of a conference telephone or other communications technology allowing all persons participating in the meeting to communicate simultaneously.</li>
    <li>Ten (10) Directors shall constitute a quorum for the transaction of business at a Board meeting, and any act of a majority of those Directors present at a meeting shall be the act of the Board.</li>
</ol>

<h4>SEC. 3. VACANCIES.</h4>

<p>Should any Director position become vacant before the term of that Director has expired, the President, in consultation with the Nominating &amp; Governance Committee, may designate an individual to fill such vacancy. The designee’s role shall be non-voting unless and until the Board ratifies the appointment. If the President is unable to complete the full term for any reason whatsoever, the Vice President of Operations shall perform the duties of the President until such time as a new President is elected pursuant to Articles XII and XIII.</p>

<h4>SEC. 4. VOTING.</h4>

<p>The majority vote of the Directors present at a meeting shall be the act of the Board, provided that a quorum is present. In addition, any action may be taken without an in-person meeting if all Directors consent in writing (by mail or electronic mail) to the adoption of the action. In all such cases, the votes will be confirmed and recorded by the Associate Vice President.</p>

<h4>SEC. 5. RESIGNATION AND REMOVAL.</h4>

<ol>
    <li>A Director may resign from the Board by submitting a written resignation to the President.</li>
    <li>A Director may be removed by a two-thirds vote of the Board.</li>
    <li>A Director who resigns or is removed will be prohibited from participating in any matters related to the Board thereafter.</li>
</ol>

<h4>SEC. 6. INDEMNIFICATION AND INSURANCE</h4>

<p>Vassar College shall indemnify all Directors against expenses or damages incurred in connection with the defense of any action, suit, or proceeding in which that person is made a party by reason of being or having been a Director, except in relation to matters as to which that person shall have been adjudged to be liable for gross negligence or misconduct in the performance of a duty. Vassar College shall maintain both general liability insurance and directors and officers liability insurance for the activities of the Association, including those of regional clubs (<em>see</em>&nbsp;Article X).</p>

<h3>ARTICLE V. OFFICERS</h3>

<h4>SEC. 1. OFFICERS.</h4>

<p>The Officers of the Association shall be the three individuals elected by the membership to hold the following positions for four-year terms.</p>

<h4>SEC. 2. POWERS AND DUTIES.</h4>

<p>The powers and duties of the Officers shall be such as the Board may prescribe, in addition to the following specified powers and duties:</p>

<ol>
    <li>The President shall be the chief executive officer of the Association. The President shall be the chair of the Board and shall preside at all meetings of the Board, the Annual Meeting of the Association, the Executive Committee, and the Cabinet. The President is an&nbsp;<em>ex officio</em>&nbsp;member of all committees and subcommittees. The President serves as one of the six Alumnae/i Association Trustees (<em>see</em>&nbsp;Article XI).</li>
    <li>The two Vice Presidents shall support the work of the President, including oversight of internal matters of the Association and Board, including the orientation process, training, oversight of Directors, effective collaboration with OAAD staff and other College departments. The Vice Presidents shall each serve as the primary liaison to the chairs of several Board committees, as designated by the President. In the event of the absence or incapacity of the President, the Vice President who has more seniority on the Board shall exercise the powers and perform the duties of the President.</li>
    <li>The Chair of the Nominating &amp; Governance Committee shall lead the Nominating &amp; Governance Committee (<em>see</em>&nbsp;Article IX, Sec. 2), and shall also support the Presidents and Vice Presidents on matters of internal Board governance and management.</li>
</ol>

<h4>SEC. 3. VACANCIES.</h4>

<ol>
    <li>The resignation of an officer shall be deemed also to be a resignation of that officer’s Directorship.</li>
    <li>In the event of the absence or incapacity of either Vice President, the remaining Vice President shall fulfill both Vice Presidents’ responsibilities.</li>
</ol>

<h3>ARTICLE VI. ASSOCIATE VICE PRESIDENT&nbsp;OF ALUMNAE/I ENGAGEMENT</h3>

<p>The Associate Vice President shall supervise the activities of the Association in accordance with policies and procedures established by the Board, and shall be responsible for (i) the records of the Association; (ii) the minutes of meetings of the Board and the Annual Meeting; (iii) meeting notices; (iv) and the official correspondence of the Association. The Board shall advise the Office of Alumnae/i Affairs and Development on the appointment and retention of the Associate Vice President. The Associate Vice President will be an employee of Vassar College, reporting directly to the Vice President for Alumnae/i Affairs and Development.</p>

<h3>ARTICLE VII. EDITOR OF COLLEGE MAGAZINE</h3>

<p>The Editor of Vassar College’s official magazine will be an employee of Vassar College, reporting directly to the Vice President for Communications. The Communications Advisory Committee shall advise the Office of Alumnae/i Affairs and Development and the Office of Communications on the appointment and retention of the Editor.</p>

<h3>ARTICLE VIII. MEETINGS OF MEMBERS OF THE ASSOCIATION</h3>

<h4>SEC. 1. ANNUAL MEETING.</h4>

<p>The Annual Meeting of the members of the Association for the election of Directors and the transaction of any other business shall be held at Vassar College at a time to be determined by the Board. Presently, the Annual Meeting is held on Reunion Weekend each June. In exceptional circumstances, as determined by the Executive Committee, the Annual Meeting may be conducted electronically.&nbsp;Notice of each Annual Meeting shall be given at least thirty (30) days in advance.</p>

<h4>SEC. 2. SPECIAL MEETINGS.</h4>

<p>Special meetings of the Association shall be held at Vassar College at a time to be determined by the Board and may be called by the Board or upon the written request of four hundred (400) members, representing at least fifteen (15) classes and ten (10) states. Such a request shall be filed with the Associate Vice President. The date for such meeting shall not be less than two (2) months, nor more than three (3) months, from the date of such written request. Notice of such meeting shall be given at least thirty (30) days prior to the date of such meeting and shall give notice of the business to come before the meeting.</p>

<h4>SEC. 3. QUORUM.</h4>

<p>Except as may be otherwise specifically required by these Bylaws, four hundred (400) members of the Association shall constitute a quorum for the transaction of business at an Association meeting, and any act of a majority of the members present at a meeting shall be an act of the Association.</p>

<h3>ARTICLE IX. COMMITTEES</h3>

<h4>SEC. 1. EXECUTIVE COMMITTEE AND OFFICERS.</h4>

<ol>
    <li>The Executive Committee shall consist of the President, who shall be Chair, the two Vice Presidents, and the Chairs of all committees. In the event that major policy decisions are required between full Board meetings, the Executive Committee shall be consulted and may act on behalf of the Board in carrying out the business of the Association. The staff liaison for the Executive Committee shall be the Associate Vice President, or such other individual so designated by the Board.</li>
    <li>The Officers shall consist of the President, who shall be Chair, two Vice Presidents, and the Chair of the Nominating &amp; Governance Committee. Between full Board meetings, the Officers shall make tactical decisions not requiring the input of the full Board or Executive Committee. The staff liaison for the Officers shall be the Associate Vice President, or such other individual so designated by the Board.</li>
</ol>

<h4>SEC. 2. NOMINATING &amp; GOVERNANCE COMMITTEE.</h4>

<ol>
    <li>The Nominating &amp; Governance Committee shall, in consultation with the President,&nbsp;nominate alumnae/i to fill (i) Officer Positions (<em>see</em>&nbsp;Article V); (ii) Director positions (<em>see</em>&nbsp;Article IV); (iii) Alumnae/i Association Trustees (<em>see</em>&nbsp;Article XI); (iv) non-Director committee memberships; and (v) other roles as requested from time to time by the Officers.</li>
    <li>Within 30 days of the Annual Meeting, and in consultation with the Officers, the Committee shall select Chairs for the Committees of the Board from among the active Directors, as well as appoint Director and non-Director members of committees.</li>
    <li>In addition, the Nominating &amp; Governance Committee shall advise the Board on broader governance matters, including amendments to these Bylaws.</li>
    <li>The Nominating &amp; Governance Committee shall consist exclusively of Directors. The Chair shall be selected by the President in consultation with the Vice Presidents for a four-year term. The other committee members shall be appointed to the Committee by the Nominating &amp; Governance Committee in consultation with the Officers for two-year renewable terms. The staff liaison for the Nominating &amp; Governance Committee shall be the Associate Vice President, or such other individual so designated by the Board.</li>
</ol>

<h4>SEC. 3. THE VASSAR FUND COMMITTEE.</h4>

<ol>
    <li>The Vassar Fund Committee shall advise on the development and implementation of strategies to encourage the financial support of Vassar College.</li>
    <li>The Vassar Fund Committee shall consist of a Chair selected from among the Directors by the Nominating &amp; Governance Committee for a four-year term, as well as other committee members appointed for two-year renewable terms. Committee members may include Directors as well as members of the Association who are not serving as Directors. The staff liaison for the Vassar Fund Committee shall be the Director of the Vassar Fund, or such other individual so designated by the Associate Vice President in consultation with the Officers.</li>
</ol>

<h4>SEC. 4. ALUMNAE HOUSE COMMITTEE.</h4>

<ol>
    <li>The Alumnae House Committee shall advise Vassar College on matters related to Alumnae House, and shall provide guidance on developing and implementing strategies for engaging alumnae/i and current students with Alumnae House.</li>
    <li>The Alumnae House Committee shall consist of a Chair selected from among the Directors by the Nominating &amp; Governance Committee for a four-year term, as well as other committee members appointed for two-year renewable terms. Committee members may include Directors as well as members of the Association who are not serving as Directors. The staff liaison for the Alumnae House Committee shall be the Manager of the Alumnae House, or such other individual so designated by the Associate Vice President in consultation with the Officers.</li>
</ol>

<h4>SEC. 5 COMMUNICATIONS ADVISORY COMMITTEE.</h4>

<ol>
    <li>The Communications Advisory Committee shall provide strategic advice to the Board and Vassar College, including the Office of Communications and Office of Alumnae/i Affairs and Development, on alumnae/i communications and branding strategies.</li>
    <li>The Communications Advisory Committee shall consist of a Chair selected from among the Directors by the Nominating &amp; Governance Committee for a four-year term, as well as other committee members appointed for two-year terms. Committee members may include Directors as well as members of the Association who are not serving as Directors. The staff liaison for the Communications Advisory Committee shall be the Director of Alumnae/i Communications, or such other individual so designated by&nbsp;the Associate Vice President in consultation with the Officers.</li>
</ol>

<h4>SEC. 6. ALUMNAE/I RECOGNITION COMMITTEE.</h4>

<ol>
    <li>The Alumnae/i Recognition Committee shall select members of the College community to receive designated annual awards. These awards are presently defined as: (i) the Distinguished Achievement Award; (ii) the Outstanding Service to Vassar Award; (iii) the Spirit of Vassar Award; (iv) the Young Alumnae/i Achievement and/or Service Award; and (v) the Outstanding Faculty/Staff Award.&nbsp;These awards may be amended from time to time.</li>
    <li>The Alumnae/i Recognition Committee shall, from time to time, review the categories of awards and propose modifications to the Board.</li>
    <li>The Alumnae/i Recognition Committee shall consist of a Chair selected from among the Directors by the Nominating &amp; Governance Committee for a four-year term, as well as other committee members appointed for two-year renewable terms. Committee members may include Directors as well as members of the Association who are not serving as Directors. The staff liaison for the Alumnae/i Recognition Committee shall be the Associate Vice President, or such other individual so designated by the Board.</li>
</ol>

<h4>SEC. 7. CAREER NETWORKING COMMITTEE.</h4>

<ol>
    <li>The Career Networking Committee shall advise Vassar College, including its Office of Career Development and its Office of Alumnae/i Affairs and Development, on services that engage alumnae/i in support of students’ career development, provide career support to alumnae/i, and strengthen alum-to-alum professional networks.</li>
    <li>The Career Networking Committee shall consist of a Chair selected from among the Directors by the Nominating &amp; Governance Committee for a four-year term, as well as other committee members appointed for two-year renewable terms. Committee members may include Directors as well as members of the Association who are not serving as Directors. The staff liaisons for the Career Networking Committee shall be the Senior Director of Alumnae/i Engagement and the Director of the Career Development Office, or such other individuals so designated by the Associate Vice President in consultation with the Officers.</li>
</ol>

<h4>SEC. 8. CLUBS COMMITTEE.</h4>

<ol>
    <li>The Clubs Committee shall advise the Office of Alumnae/i Affairs and Development on Vassar’s presence regionally and around the world, and develop and implement strategies to engage alumnae/i in their communities.</li>
    <li>The Clubs Committee shall consist of a Chair selected from among the Directors by the Nominating &amp; Governance Committee for a four-year term, as well as other committee members appointed for two-year renewable terms. Committee members may include Directors as well as members of the Association who are not serving as Directors. Each member should be a Club President or designated regional representative. The staff liaison for the Clubs Committee shall be the Associate Director of Alumnae/i Engagement, or such other individual so designated by the Associate Vice President in consultation with the Officers.</li>
</ol>

<h4>SEC. 9. CLASS ENGAGEMENT COMMITTEE.</h4>

<ol>
    <li>The Class Engagement Committee shall advise the Office of Alumnae/i Affairs and Development on services that engage alumnae/i through class affiliation to strengthen engagement of all members of the Association. It shall provide guidance on class leadership structure, class programming, and Reunion activities.</li>
    <li>The Class Engagement Committee shall consist of a Chair selected from among the Directors by the Nominating &amp; Governance Committee for a four-year term, as well as other committee members appointed for two-year renewable terms. Committee members may include Directors as well as members of the Association who are not serving as Directors. The staff liaison for the Class Engagement Committee shall be the Associate Director of Alumnae/i Engagement or such other individual so designated by the Associate Vice President in consultation with the Officers.</li>
</ol>

<h4>SEC. 10. SPECIAL COMMITTEES AND TASK FORCES.</h4>

<p>The Officers of the Board may create special committees and task forces of the Board or the Association, designating the purpose and powers of such groups. The Officers, in consultation with the Nominating &amp; Governance Committee, shall appoint the chair(s) of each special committee or task force. The terms of membership on special committees shall be set forth in the resolution designating the purpose and powers of such committees, but shall not exceed one year unless so approved by the Officers.</p>

<h4>SEC. 11. OBSERVERS TO THE BOARD.</h4>

<ol>
    <li>From time to time, the Board may approve groups or associations of alumnae/i as observers to the Board.</li>
    <li>The African American Alumnae/i of Vassar College (“AAAVC”) was formed in 1984 as a steering committee. The AAAVC steering committee has two co-chairs, who shall serve as observers to the Board and may fully participate in discussions at meetings, but without any voting rights.</li>
    <li>Two students – the Senior Class President and a member of the Vassar Student Association (“VSA”) Executive Board selected by the VSA – shall serve as observers to the Board and may fully participate in discussions at meetings, but without any voting rights.</li>
</ol>

<h3>ARTICLE X. VASSAR CLUBS</h3>

<p>Upon the approval of the Clubs Committee (<em>see</em>&nbsp;Article IX, Sec. 8), Vassar alumnae/i may form clubs in strategic regions. Such clubs shall produce programs to enhance the personal, professional, and social experiences of local alumnae/i, and maintain connection between local alumnae/i and the College. The Clubs Committee shall review and evaluate the activities of domestic and international Vassar clubs. These groups shall have such budgets provided by the Office of Alumnae/i Affairs and Development. The budget allocations will be determined, from time to time, in consultation with the Clubs Committee.</p>

<h3>ARTICLE XI. ALUMNAE/I ASSOCIATION TRUSTEES OF VASSAR COLLEGE</h3>

<h4>SEC. 1. DEFINITION.</h4>

<p>Six members of the Association nominated by the Nominating &amp; Governance Committee, in consultation with the President, and elected by the Association at the Annual Meeting shall serve as Alumnae/i Association Trustees of Vassar College (“Association Trustees”).</p>

<h4>SEC. 2. NUMBER AND TERM.</h4>

<p>There shall be six (6) Association Trustees, each of whom shall be eligible to serve for the standard term of a trustee of Vassar College (presently, four years), and one of whom shall be President of the Association. Association Trustees shall have the same rights, powers, responsibilities, obligations, liabilities, and coverage as the members of the Board of Trustees, and shall agree to the same in the same manner as the members of the Board of Trustees prior to commencement of their terms.</p>

<h4>SEC. 3. NOMINATIONS.</h4>

<p>From the nominee(s) presented by the Nominating &amp; Governance Committee, the members of the Association shall elect individuals to be Association Trustees, subject to ratification by the Board of Trustees. The Nominating &amp; Governance Committee shall nominate one Director to serve as Association Trustee in each even year (e.g., 2024) and two Directors in each odd year (e.g., 2025), unless vacancies require the nomination of additional candidates. The individual elected as President of the Association shall be deemed to be the nominee for a concurrent four-year term as an Association Trustee.</p>

<h3>ARTICLE XII. NOMINATIONS TO THE BOARD OF DIRECTORS</h3>

<h4>SEC. 1. NOMINATIONS BY THE NOMINATING &amp; GOVERNANCE COMMITTEE.</h4>

<ol>
    <li>The Nominating &amp; Governance Committee, in consultation with the President, shall nominate members of the Association to serve as Directors in accordance with procedures adopted by the Board.</li>
    <li>The positions for which the Nominating &amp; Governance Committee must put forth a slate of candidates include: (i) the Officers (<em>see</em>&nbsp;Article V); (ii) the Directors (<em>see</em>&nbsp;Article IV); and (iii) the Association Trustees.</li>
    <li>The Nominating &amp; Governance Committee shall identify members to fill all positions where the term of a sitting Director will expire. No nominee may be put forth for election by the membership of the Association without that nominee’s prior consent.</li>
</ol>

<h4>SEC. 2. INDEPENDENT NOMINATIONS.</h4>

<p>Nominations may also be made by petition to be signed by not less than four hundred (400) members of the Association representing at least fifteen (15) classes and ten (10) states, and filed with the Associate Vice President not more than thirty (30) days after publication of the slate of the Nominating &amp; Governance Committee. Such petition must be accompanied by the written permission of the candidate.</p>

<h3>ARTICLE XIII. ELECTIONS OF INDIVIDUALS NOMINATED TO THE BOARD</h3>

<h4>SEC. 1. METHOD.</h4>

<p>The names of all nominees for Directors, Officers and Association Trustees nominated pursuant to Article XII shall be announced on the Association’s website,&nbsp;<a href="https://vassar.edu/alums">vassar.edu/alums</a>, or by any other means deemed appropriate by the Nominating &amp; Governance Committee, at least forty-five (45) days before the Annual Meeting, together with notice of the right to make independent nominations pursuant to Article XII, Sec. 2.</p>

<h4>SEC. 2. VOTING.</h4>

<p>The membership shall vote during the Annual Meeting by voice vote, at a time and place prescribed by the Board, on the slate presented by the Nominating &amp; Governance Committee. A majority approval of the slate shall constitute ratification. In exceptional circumstances, in a manner determined by the Executive Committee, such voting may take place electronically.</p>

<h3>ARTICLE XIV. AMENDMENTS</h3>

<ol>
    <li>These Bylaws may be amended by a two-thirds vote of the Board. Notice of proposed amendments shall be given to Directors at least thirty (30) days before such vote.</li>
    <li>Any amendment(s) approved by the Board shall be ratified by a majority of the votes cast by the members of the Association during the Annual Meeting.</li>
    <li>Notice of the proposed Bylaw changes shall be announced on the Association’s website,&nbsp;<a href="https://vassar.edu/alums">vassar.edu/alums</a>, or by any other means deemed appropriate by the Nominating &amp; Governance Committee, at least forty-five (45) days before the Annual Meeting.</li>
    <li>Such amendment(s) shall take effect upon ratification at the Annual Meeting.</li>
</ol>

<p><em>The Alumnae/i Association Bylaws were adopted in June 1980, and amended in June 1992; June 2000; June 2005; June 2008; June 2013; June 2016; June 2018; and June 2020.</em></p>

